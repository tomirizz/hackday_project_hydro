/** Перевод с подстановкой переменных {name}. */
export function L(lang, key, vars) {
  const dict = A_STRINGS[lang] || A_STRINGS.ru;
  let s = dict[key] ?? A_STRINGS.ru[key] ?? key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), vars[k]);
    }
  }
  return s;
}
